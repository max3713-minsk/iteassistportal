const DEFAULT_HINT: ProblemHint = {
  description: "Активная проблема мониторинга. Требуется анализ причин.",
  recommendation:
    "Диагностика и устранение силами Исполнителя в рамках регламента; материалоёмкие работы — по запросу Заказчика.",
};

export function getProblemHint(text: string): ProblemHint {
  const s = (text || "").toString();
  for (const r of RULES) {
    if (r.test.test(s)) return r.hint;
  }
  return DEFAULT_HINT;
}

